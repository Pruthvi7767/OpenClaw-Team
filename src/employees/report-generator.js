const PDFDocument = require('pdfkit');
const fs = require('fs');
const path = require('path');
const supabase = require('../services/supabase');
const llm = require('../services/llm');
const logger = require('../utils/logger');

class ReportGenerator {
    async generateForPaid() {
        logger.info('📄 Generating reports...');

        const { data: clients } = await supabase.client
            .from('clients')
            .select('*')
            .eq('payment_status', 'paid')
            .eq('report_sent', false);

        if (!clients || clients.length === 0) return [];

        const reports = [];

        for (const client of clients) {
            try {
                const file = await this.createReport(client);

                reports.push({ client_id: client.id, path: file });

                await supabase.client
                    .from('clients')
                    .update({ status: 'report_ready' })
                    .eq('id', client.id);

            } catch (err) {
                logger.error('Report failed:', err);
            }
        }

        return reports;
    }

    async createReport(client) {
        const data = client.scraped_data || {};
        const score = this.calculateScore(data);
        const ai = await this.generateAIInsights(client, data, score);

        const file = `/tmp/report_${client.id}.pdf`;
        const doc = new PDFDocument({ margin: 50 });

        doc.pipe(fs.createWriteStream(file));

        const primary = '#2563eb';
        const red = '#dc2626';
        const green = '#16a34a';
        const gray = '#6b7280';

        // ===== COVER =====
        doc.rect(0, 0, doc.page.width, 180).fill(primary);

        doc.fillColor('white')
            .fontSize(28)
            .text('MARKLY', 50, 60);

        doc.fontSize(18)
            .text('Business Growth Report', 50, 100);

        doc.moveDown(2);

        doc.fillColor('black')
            .fontSize(14)
            .text(client.company_name);

        doc.fontSize(10).fillColor(gray)
            .text(client.website);

        doc.moveDown(3);

        // ===== SCORE =====
        doc.fontSize(18).fillColor(primary)
            .text(`Overall Score: ${score}/100`);

        this.drawScoreBar(doc, score);

        doc.moveDown(2);

        // ===== SUMMARY =====
        this.section(doc, 'Executive Summary');
        doc.fontSize(11).fillColor('black')
            .text(ai.summary, { align: 'justify' });

        doc.addPage();

        // ===== PROBLEMS =====
        this.section(doc, 'Key Problems');
        ai.problems.forEach(p => {
            doc.fillColor(red).text(`• ${p}`);
            doc.moveDown(0.5);
        });

        doc.moveDown(2);

        // ===== QUICK WINS =====
        this.section(doc, 'Quick Wins');
        ai.quickWins.forEach(w => {
            doc.fillColor(green).text(`• ${w}`);
            doc.moveDown(0.5);
        });

        doc.moveDown(2);

        // ===== ACTION PLAN =====
        this.section(doc, 'Action Plan');
        ai.actions.forEach(a => {
            doc.fillColor('black').text(`• ${a}`);
            doc.moveDown(0.5);
        });

        doc.addPage();

        // ===== METRICS =====
        this.section(doc, 'Website Snapshot');

        doc.fillColor('black').fontSize(11)
            .text(`• Mobile Friendly: ${data.mobileFriendly ? 'Yes' : 'No'}`)
            .text(`• SSL Secure: ${data.hasSSL ? 'Yes' : 'No'}`)
            .text(`• CTA Present: ${data.hasCTA ? 'Yes' : 'No'}`)
            .text(`• Testimonials: ${data.hasTestimonials ? 'Yes' : 'No'}`)
            .text(`• Load Time: ${data.loadTime || 'N/A'} sec`);

        doc.moveDown(2);

        // ===== MARKET =====
        this.section(doc, 'Market Insight');
        doc.text(ai.market, { align: 'justify' });

        doc.moveDown(3);

        // ===== FOOTER =====
        doc.fontSize(10).fillColor(gray)
            .text('Generated by Markly', { align: 'center' });

        doc.text(process.env.GMAIL_USER, {
            align: 'center',
            link: `mailto:${process.env.GMAIL_USER}`
        });

        doc.end();

        return file;
    }

    section(doc, title) {
        doc.fillColor('#2563eb')
            .fontSize(14)
            .text(title);
        doc.moveDown(0.5);
    }

    drawScoreBar(doc, score) {
        const width = 300;
        const height = 10;
        const x = 50;
        const y = doc.y;

        doc.rect(x, y, width, height).fill('#e5e7eb');

        const filled = (score / 100) * width;
        doc.rect(x, y, filled, height).fill('#2563eb');

        doc.moveDown(2);
    }

    calculateScore(data) {
        let score = 100;

        if (!data?.hasCTA) score -= 20;
        if (!data?.mobileFriendly) score -= 15;
        if (!data?.hasTestimonials) score -= 10;
        if (data?.loadTime > 4) score -= 20;

        return Math.max(score, 30);
    }

    async generateAIInsights(client, data, score) {
        const prompt = `
You are a business growth expert.

Website data:
${JSON.stringify(data)}

Score: ${score}

Generate:

1. Summary (short, sharp)
2. 3 problems
3. 3 quick wins
4. 4 actions
5. market insight

Be direct, no fluff.
`;

        const res = await llm.generate(prompt);

        return this.parse(res);
    }

    parse(text) {
        const lines = text.split('\n').filter(Boolean);

        return {
            summary: lines.slice(0, 3).join(' '),
            problems: lines.slice(3, 6),
            quickWins: lines.slice(6, 9),
            actions: lines.slice(9, 13),
            market: lines.slice(13).join(' ')
        };
    }
}

module.exports = new ReportGenerator();